    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String str3;
        int s, sn;
        
        str3 = JOptionPane.showInputDialog(null, "Masukkan Jumlah yang ingin anda setor : ");
        s = Integer.parseInt(str3);
        sn = Integer.parseInt(Saldo.getText());
        
        if(s%100!=0)
            JOptionPane.showMessageDialog(null, 
                    "Mesin ATM BNI tidak menerima uang koin \nSilahkan ulangi kembali!",
                    "Transaksi Gagal",0);
        else if(s<50000)
            JOptionPane.showMessageDialog(null, 
                    "Besaran minimal setor tunai adalah Rp. 50000,00","ERROR",0);
        else if (s>=50000)
        {
            int total = sn + s;
            JOptionPane.showMessageDialog(null,"Setor tunai anda berhasil!","Berhasil",0);
            Saldo.setText(""+total);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

