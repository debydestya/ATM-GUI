    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String str1;
        int t, sn;
        
        str1 = JOptionPane.showInputDialog(null, "Masukkan jumlah penarikan : "); 
        t = Integer.parseInt(str1);
        sn = Integer.parseInt(Saldo.getText());
        
        if (t>sn)
            JOptionPane.showMessageDialog(null, "Saldo anda tidak mencukupi \nSilahkan lakukan penyetoran","Transaksi Gagal",0);  
        else if (t<50000)
            JOptionPane.showMessageDialog(null, "Pengambilan tunai minimal adalah Rp.50000","Transaksi Gagal",0);
        else
        {
            int total = sn - t;
            JOptionPane.showMessageDialog(null,"Penarikan tunai anda berhasil!","Berhasil",0);
            Saldo.setText(""+total);
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

